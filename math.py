x = 10
y = 3

# tambah 
z = x + y
print("Hasil Pertambahan {} + {} = {}".format(x,y,z))

# kurang 
a = x - y
print("Hasil Pengurangan {} - {} = {}".format(x,y,a))

# kali 
b = x * y
print("Hasil Perkalian {} * {} = {}".format(x,y,b))

# bagi
c = x / y
print("Hasil Pembagian {} / {} = {}".format(x,y,c))

# modulo
d = x % y
print("Hasil Modulo {} % {} = {}".format(x,y,d))

# pangkat
e = x ** y
print("Hasil Perpangkatan {} ** {} = {}".format(x,y,e))




